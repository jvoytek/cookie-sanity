CREATE OR REPLACE FUNCTION "public"."handle_new_user"() 
    RETURNS "trigger"
    LANGUAGE "plpgsql" 
    SECURITY DEFINER
    SET "search_path" TO ''
    AS $$
begin
  insert into public.profiles (id)
  values (new.id);
  return new;
end;
$$;


